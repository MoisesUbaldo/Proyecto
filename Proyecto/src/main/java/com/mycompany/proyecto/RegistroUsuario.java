    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

     
       
       
       
         String usuario = txtUsuario.getText().trim();
        String age = txtAge.getText().trim();

        String contra= String.valueOf(txtPass.getPassword());

        //campos vacios
        if ((usuario.equals("")) || (contra.equals(""))|| (age.equals("")) ||sexo.equals("")  ) {

            JOptionPane.showMessageDialog(null, "Campos vacios");
           
            //validar usuario
        } else {
            
        
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/login", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into usuario values(?,?,?,?,?)");
            
            pst.setString(1, "0");
            pst.setString(2, txtUsuario.getText().trim().toLowerCase());
            pst.setString(3, contra.toLowerCase());
            pst.setString(4, txtAge.getText());
            pst.setString(5, sexo);
            
            pst.executeUpdate();
           
        
        }catch (Exception e){
            
        }     

        this.setVisible(false);
        
         Login lg = new Login();
            lg.setVisible(true);
            
            
        }

            
        
    }//GEN-LAST:event_jButton1ActionPerformed

