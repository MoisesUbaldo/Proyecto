    public void validaUsuario(JTextField usuario, JPasswordField contrasenia) {

        try {
            ResultSet rs = null;
            PreparedStatement ps = null;

            local.ClaseConexion objetoConexion = new local.ClaseConexion();

            String consulta = "select * from usuario where usuario.ingresoUsuario =(?) and usuario.ingresoContrasenia=(?);";
            ps = objetoConexion.estableceConexion().prepareStatement(consulta);

            String contra = String.valueOf(contrasenia.getPassword());

            ps.setString(1, usuario.getText());
            ps.setString(2, contra);

            rs = ps.executeQuery();

            if (rs.next()) {

                JOptionPane.showMessageDialog(null, "El usuario es correcto");
                this.setVisible(false);
                
                Menu objetoMenu = new Menu();
                objetoMenu.setVisible(true);

            } else {
                this.setVisible(true);

                JOptionPane.showMessageDialog(null, "El Usuario es incorrecto, vuelva a intentar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e.toString());
        }
    }
    
    
